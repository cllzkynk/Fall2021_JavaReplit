package Replit_01;

public class C04 {

	public static void main(String[] args) {
		/*
		 * Dairenin alanini ve cevresini hesaplayan java kodunu yaziniz.
		 * 
		 * r=7;
		 * 
		 * Pi=3.14
		 * 
		 * Ipucu:
		 * 
		 * Dairenin Cevresi : 2*Pi*r
		 * 
		 * Dairenin Alani : Pi*r*r
		 * 
		 * Ornek Cikti:
		 * 
		 * 43.96
		 * 
		 * 153.86
		 */

		double r = 7;
		double pi = 3.14;

		System.out.println(2 * pi * r);
		System.out.println(pi * r * r);

	}

}
