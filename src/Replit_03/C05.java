package Replit_03;



public class C05 {

	public static void main(String[] args) {
		/*
		 * 100'den 0'a kadar cift sayilari yazdirin,
		 * ancak decrement(i--) kullanmayin.
		 * 
		 * OUTPUT : 100 98 96 94 92 ... 2 0
		 */

		for (int i = 100; i >=0; i-=2) {
			System.out.print(i+ " ");
		}


		
		
		
		
		
		
		
		
		
	}
		
		
	}


